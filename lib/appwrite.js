// Obtener video que se filtren el la barra de busqueda
export async function searchPosts(query) {
  try {
    const posts = await databases.listDocuments(
      config.databaseId,
      config.videoCollectionId,
      [Query.search("title", query)]
    );

    if (!posts) throw new Error("Something went wrong");

    return posts.documents;
  } catch (error) {
    throw new Error(error);
  }
}

// Obtener el ultimo video subido
export async function getLatestPosts() {
  try {
    const posts = await databases.listDocuments(
      config.databaseId,
      config.videoCollectionId,
      [Query.orderDesc("$createdAt"), Query.limit(7)]
    );

    return posts.documents;
  } catch (error) {
    throw new Error(error);
  }
}

// Report Video
export async function reportVideo(videoId, reason) {
  try {
    const report = await databases.createDocument(
      config.databaseId,
      config.reportsCollectionId,
      ID.unique(),
      {
        videoId: videoId,
        reason: reason,
        timestamp: new Date().toISOString(),
      }
    );

    return report;
  } catch (error) {
    throw new Error("Failed to report video: " + error.message);
  }
}

// Descargar video
export async function downloadVideo(videoUrl) {
  try {
    const fileUri = FileSystem.documentDirectory + "video.mp4"; // Puedes personalizar el nombre del archivo
    const downloadResult = await FileSystem.downloadAsync(videoUrl, fileUri);
    return downloadResult.uri;
  } catch (error) {
    throw new Error("Failed to download video: " + error.message);
  }
}
